    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String maNhanVien = txtMaNhanVien.getText();
        String hoTen = txtHoTen.getText();
        String gioiTinh = txtGioiTinh.getText();
        String diaChi = txtDiaChi.getText();
        String namSinh = txtNamSinh.getText();
        String congViec = txtCongViec.getText();
        if(maNhanVien.compareTo("") == 0 || hoTen.compareTo("") == 0 || gioiTinh.compareTo("") == 0 || diaChi.compareTo("") == 0 || namSinh.compareTo("") == 0 || congViec.compareTo("") == 0){
            JOptionPane.showMessageDialog(rootPane, "Bạn phải điền đầy đủ thông tin của nhân viên");
        } else {
            NhanVien nv = new NhanVien();
            nv.setMaCB(maNhanVien);
            nv.setHoTen(hoTen);
            nv.setGioiTinh(gioiTinh);
            nv.setDiaChi(diaChi);
            nv.setNamSinh(Integer.parseInt(namSinh));
            nv.setCongViec(congViec);
            if(new NhanVienModify().themNhanVien(nv)){
                JOptionPane.showMessageDialog(rootPane, "Đã thêm thành công");
                listNhanVien.add(nv);
                showTable();
            } else{
                JOptionPane.showMessageDialog(rootPane, "Thêm mới thất bại");
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

