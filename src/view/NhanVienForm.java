    private void showTable() {
        listNhanVien = new NhanVienModify().getListNhanVien();
        dtm.setRowCount(0);
        for(NhanVien nv : listNhanVien){
            dtm.addRow(new Object[]{
                nv.getMaCB(),
                nv.getHoTen(),
                nv.getGioiTinh(),
                nv.getDiaChi(),
                nv.getNamSinh(),
                nv.getCongViec()
            });
        }
    }

